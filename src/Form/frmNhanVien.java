    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        
        if(searchMaNV(txtID.getText().trim()))
        {
            JOptionPane.showMessageDialog(rootPane, "Mã nhân viên đã tồn tại");
            return;
        }
        else if(txtTenNV.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(rootPane, "Tên nhân viên trống");
            return;
        }
        else if(txtMatKhau.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu trống");
                        return;
        }
        else if(txtChuc.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(rootPane, "Chức vụ của nhân viên là gì?");
                        return;
        }
        
        NhanVien nv = new NhanVien(txtID.getText(), txtMatKhau.getText(), txtTenNV.getText(), txtChuc.getText());
        nhanVien = new table_NhanVien();
        
        try {
            if(nhanVien.insertMatHang(nv))
            {
                JOptionPane.showMessageDialog(rootPane, "Add success.");
                LoadDataBase();
                Clear();
            }
        } catch (SQLException ex) {
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnInsertActionPerformed

