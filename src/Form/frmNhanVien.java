    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        // TODO add your handling code here:
        table = (DefaultTableModel)tblNhanVien.getModel();
        
        int index = tblNhanVien.getSelectedRow();
        
        if(index > -1){
            txtID.setText(table.getValueAt(index, 0).toString());
            txtTenNV.setText(table.getValueAt(index, 2).toString());
            txtMatKhau.setText(table.getValueAt(index, 1).toString());
            txtChuc.setText(table.getValueAt(index, 3).toString());
        }
    }//GEN-LAST:event_tblNhanVienMouseClicked


    public void LoadDataBase(){
        nhanVien = new table_NhanVien();
        try{
            arrNhanVien = new ArrayList<NhanVien>();
            table = (DefaultTableModel) tblNhanVien.getModel();
            table.setRowCount(0);
            for(NhanVien nv: nhanVien.getData()){

                arrNhanVien.add(nv);
                String[] nhanvien = {nv.getId(),nv.getMatKhau(),nv.getTenNV(),nv.getChuc()};
                table.addRow(nhanvien);
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
    private void Clear(){
        txtChuc.setText("");
        txtID.setText("");
        txtTenNV.setText("");
        txtMatKhau.setText("");
    }
    
    private boolean searchMaNV(String maNV) {
        for(NhanVien nv: arrNhanVien)
        {
            if(nv.getId().equals(maNV))
                return true;
        }
        return false;
    }

