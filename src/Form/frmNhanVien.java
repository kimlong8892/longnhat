    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if(tblNhanVien.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn mặt hàng để sửa");
            return;
        }
        NhanVien nv = new NhanVien(txtID.getText(), txtMatKhau.getText(), txtTenNV.getText(), txtChuc.getText());
        nhanVien = new table_NhanVien();
        try {
            if(nhanVien.updateMatHang(nv))
            {
                JOptionPane.showMessageDialog(rootPane, "Update success.");
                LoadDataBase();
                Clear();
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmHangHoa.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmHangHoa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if(tblNhanVien.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn mặt hàng để xóa");
            return;
        }
        else{
            frame = new JFrame();
            nhanVien = new table_NhanVien();
            if(JOptionPane.showConfirmDialog(frame, "Bạn có muốn xóa","Thông báo",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                try {
                    if(nhanVien.deletetMatHang(txtID.getText().toString()))
                    {
                        JOptionPane.showMessageDialog(rootPane, "Delete success.");
                        LoadDataBase();
                        Clear();
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(frmHangHoa.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(frmHangHoa.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

