    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:

        Clear();
        if(searchMaHang(txtID.getText().trim()))
        {
            JOptionPane.showMessageDialog(rootPane, "Mã hàng đã tồn tại");
            return;
        }
        else if(txtTenHang.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(rootPane, "Tên hàng trống");
            return;
        }
        else if(txtDVT1.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(rootPane, "Đơn vị tính trống");
                        return;
        }
        else if(txtSoLuong.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(rootPane, "Nhập số lượng hàng");
                        return;
        }
                int soLuong = Integer.valueOf(txtSoLuong.getText());
                int gia = Integer.valueOf(txtGia.getText());
                MatHang mh = new MatHang(txtID.getText(), txtTenHang.getText(), txtDVT1.getText(), searchMaLoai(cboLoai.getSelectedItem().toString()), searchMaNCC(cboNCC.getSelectedItem().toString()), soLuong, gia);
                tblMatHang = new table_MatHang();
        try {
            if(tblMatHang.insertMatHang(mh))
            {
                JOptionPane.showMessageDialog(rootPane, "Add success.");
                LoadDataHangHoa();
                Clear();
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmHangHoa.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmHangHoa.class.getName()).log(Level.SEVERE, null, ex);
        }
            

        
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if(tblHangHoa.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn mặt hàng để xóa");
            return;
        }
        else{
            frame = new JFrame();
            tblMatHang = new table_MatHang();
            if(JOptionPane.showConfirmDialog(frame, "Bạn có muốn xóa","Thông báo",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                try {
                    if(tblMatHang.deletetMatHang(txtID.getText()))
                    {
                        JOptionPane.showMessageDialog(rootPane, "Delete success.");
                        LoadDataHangHoa();
                        Clear();
                    }
                } catch (SQLException | ClassNotFoundException ex) {
                    Logger.getLogger(frmHangHoa.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    
