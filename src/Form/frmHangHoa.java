    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
                    
        tblMatHang = new table_MatHang();
        
        try{
            String maLoai = searchMaLoai(cboLoai.getSelectedItem().toString());
            String maNCC = searchMaNCC(cboNCC.getSelectedItem().toString());
            String dk = " maHang = '"+txtID.getText()+"' or tenHang = '"+txtTenHang.getText()+"' or dvt = '"+txtDVT1.getText()+"' or maLoai = '"+maLoai+"' or maNCC = '"+maNCC+"'";
            //Đưa dữ liệu lên table
            table = (DefaultTableModel) tblHangHoa.getModel();
            table.setRowCount(0);
            for(MatHang mh: tblMatHang.searchData(dk)){
                String[] mathang = {mh.getId(),mh.getTenHang(),String.valueOf(mh.getSoLuong()),mh.getDvt(),String.valueOf(mh.getGia()),searchLoai(mh.getMaLoai()),searchNCC(mh.getMaNCC())};
                table.addRow(mathang);
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

