    public void LoadDataHangHoa(){
        
        tblMatHang = new table_MatHang();
        tblLoai = new table_LoaiHang();
        tblNCC = new table_NCC();
        
        try{
            
            arrNCC = new ArrayList<NCC>();
            arrlh = new ArrayList<LoaiHang>();
            
            DefaultComboBoxModel<String> dftComboBoxLoai =(DefaultComboBoxModel<String>) cboLoai.getModel();
            dftComboBoxLoai.removeAllElements();
            for(LoaiHang loai: tblLoai.getData())
            {
                arrlh.add(loai);
                cboLoai.addItem(loai.getTenLoai());
            }
            
            DefaultComboBoxModel<String> dftComboBoxNCC =(DefaultComboBoxModel<String>) cboNCC.getModel();
            dftComboBoxNCC.removeAllElements();
            for(NCC n: tblNCC.getData())
            {
                arrNCC.add(n);
                cboNCC.addItem(n.getTenNCC());
            }
            
            //Đưa dữ liệu lên table
            table = (DefaultTableModel) tblHangHoa.getModel();
            tblHangHoa.setRowHeight(50);
            table.setRowCount(0);
            for(MatHang mh: tblMatHang.getData()){
                String[] mathang = {mh.getId(),mh.getTenHang(),String.valueOf(mh.getSoLuong()),mh.getDvt(),String.valueOf(mh.getGia()),searchLoai(mh.getMaLoai()),searchNCC(mh.getMaNCC())};
                table.addRow(mathang);
                System.out.println(mh);
            }
            
        }catch(ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
